// Operadores aritméticos

/*
    Soma (+)
    Subtração (-)
    Multiplicação (*)
    Divisão (/)
*/

let n1 = 10;
let n2 = 3;

let soma = n1 + n2;
console.log(soma);

let subtracao = n1 - n2;
console.log(subtracao);

let multiplicacao = n1 * n2;
console.log(multiplicacao);

let divisao = n1 / n2;
console.log(divisao);